#include <iostream>

int main(){
    
    std::cout << "Hello World!" << std::endl;
    std::cout << "How are you!" << std::endl;

    //integer
    int x = 10;
    std::cout << x << std::endl;

    //decimal
    float y = 3.14;
    double z = 3.14;
    std::cout << y << std::endl;
    std::cout << z << std::endl;
    
    //character
    
    char name = 'A'; // single char
    std::cout << name << std::endl;
    
    // string
    std::string Name = "Kartik";
    std::cout << Name << std::endl;
    
    //Boolean
    bool boolean1 = true;
    bool boolean2 = true;
    std::cout << boolean1 << std::endl;
    std::cout << boolean2 << std::endl;
    

    
   

    return 0;
    
    
}
